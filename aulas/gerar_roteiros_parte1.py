import os
from docx import Document
from docx.shared import Pt, RGBColor, Inches, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH

BASE = os.path.dirname(os.path.abspath(__file__))


def novo_doc(num, titulo):
    doc = Document()
    for s in doc.sections:
        s.top_margin = Cm(2.5)
        s.bottom_margin = Cm(2.5)
        s.left_margin = Cm(3.0)
        s.right_margin = Cm(2.5)
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(f"INTENSIVAO QUANT AI — AULA {num}")
    r.font.size = Pt(11); r.font.bold = True
    r.font.color.rgb = RGBColor(0xF5, 0xA6, 0x23)
    p2 = doc.add_paragraph()
    p2.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r2 = p2.add_run(titulo)
    r2.font.size = Pt(20); r2.font.bold = True
    r2.font.color.rgb = RGBColor(0x0D, 0x1B, 0x3E)
    p3 = doc.add_paragraph()
    p3.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r3 = p3.add_run("Roteiro completo — uso exclusivo do instrutor")
    r3.font.size = Pt(10); r3.font.italic = True
    r3.font.color.rgb = RGBColor(0x88, 0x88, 0x88)
    doc.add_paragraph()
    return doc


def h1(doc, t):
    p = doc.add_heading(level=1); p.clear()
    r = p.add_run(t); r.font.size = Pt(15); r.font.bold = True
    r.font.color.rgb = RGBColor(0x0D, 0x1B, 0x3E)

def h2(doc, t):
    p = doc.add_heading(level=2); p.clear()
    r = p.add_run(t); r.font.size = Pt(13); r.font.bold = True
    r.font.color.rgb = RGBColor(0x1A, 0x6E, 0xAE)

def h3(doc, t):
    p = doc.add_heading(level=3); p.clear()
    r = p.add_run(t); r.font.size = Pt(11); r.font.bold = True
    r.font.color.rgb = RGBColor(0x1E, 0x8B, 0x4C)

def fala(doc, t):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(8)
    r = p.add_run(t); r.font.size = Pt(11)
    r.font.color.rgb = RGBColor(0x1A, 0x1A, 0x1A)

def acao(doc, t):
    p = doc.add_paragraph()
    r = p.add_run(f"[{t}]")
    r.font.size = Pt(10); r.font.italic = True
    r.font.color.rgb = RGBColor(0x88, 0x44, 0x00)

def cod(doc, t):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Inches(0.4)
    r = p.add_run(t)
    r.font.name = 'Courier New'; r.font.size = Pt(10)
    r.font.color.rgb = RGBColor(0x1A, 0x6E, 0xAE)

def tempo(doc, t):
    p = doc.add_paragraph()
    r = p.add_run(f"  {t}")
    r.font.size = Pt(9); r.font.bold = True
    r.font.color.rgb = RGBColor(0xF5, 0xA6, 0x23)

def div(doc):
    p = doc.add_paragraph()
    r = p.add_run("─" * 70)
    r.font.size = Pt(8); r.font.color.rgb = RGBColor(0xCC, 0xCC, 0xCC)


