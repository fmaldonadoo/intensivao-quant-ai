    fala(doc, "Retornos financeiros tem o que chamamos de fat tails — caudas pesadas. Em uma distribuicao normal, um retorno de 3 desvios-padrao acontece em 0.3% dos dias — ou seja, menos de 1 vez por ano. Na realidade dos mercados, retornos extremos acontecem muito mais frequentemente.")
    fala(doc, "Benoit Mandelbrot — o pai dos fractais — ja documentou isso em 1963 para o mercado de algodao nos EUA. Nassim Taleb popularizou o conceito com o 'Cisne Negro'. A intuicao e simples: os mercados sao sistemas complexos com feedback e cascata de informacoes, nao lancamentos de dados independentes.")
    fala(doc, "A metrica quantitativa e a curtose em excesso. A distribuicao normal tem curtose 3 — ou 0 em excesso. Dados financeiros tipicamente tem curtose em excesso de 3 a 10. Isso significa que a probabilidade de eventos extremos e muito maior do que a gaussiana preve.")
    fala(doc, "A skewness — assimetria — tambem e importante. Retornos de acoes tipicamente tem skewness negativa: quedas sao maiores que altas em magnitude. O mercado cai mais rapido do que sobe — um fato estilizado bem documentado.")
    fala(doc, "Por que isso importa para nos? Porque o Sharpe Ratio assume normalidade. Em dados com fat tails e skewness negativa, o Sharpe subestima o risco real. E por isso que vamos reportar tambem Sortino — que penaliza so os retornos negativos — e o Max Drawdown, que captura o pior cenario.")

    acao(doc, "Avancar slide 4: Correlacoes e Estacionariedade")
    h2(doc, "Correlacoes e Estacionariedade")
    fala(doc, "Correlacoes entre acoes sao dinamicas — mudam com o tempo e com o regime de mercado. Em periodos normais, a correlacao media entre acoes do IBOVESPA fica em torno de 0.40 a 0.50. Em crises — 2008, 2020 — essa correlacao pode subir para 0.80 ou mais.")
    fala(doc, "Isso e critico para entender a diversificacao. Quando mais voce precisa de diversificacao — em uma crise — e exatamente quando ela falha. Ativos que pareciam descorrelacionados em tempos normais caem juntos quando ha stress.")
    fala(doc, "Para o nosso portfolio momentum, isso significa que em crises, mesmo selecionando os melhores do universo, podemos estar todos altamente correlacionados porque o mercado todo cai junto. E uma limitacao real da estrategia que precisa aparecer no relatorio.")
    fala(doc, "Sobre estacionariedade: precos de acoes sao series temporais nao-estacionarias. A media e a variancia mudam com o tempo — ha uma tendencia estrutural de alta. Isso torna qualquer analise estatistica diretamente sobre precos espuria — o que se chama de regressao espuria.")
    fala(doc, "Retornos, por outro lado, sao aproximadamente estacionarios. A media e estavel em torno de um valor positivo pequeno, e a variancia nao explode ao longo do tempo. O teste formal e o teste ADF — Augmented Dickey-Fuller. Retornos tipicamente rejeitam a hipotese nula de raiz unitaria com p-value muito baixo; precos tipicamente nao rejeitam.")
    fala(doc, "Regra pratica: NUNCA modele precos diretamente. Sempre transforme em retornos primeiro.")

