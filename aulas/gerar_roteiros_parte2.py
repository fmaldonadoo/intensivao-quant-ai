    cod(doc, "ret_mensais = pd.read_parquet('../../dados/retornos_mensais_limpo.parquet')")
    cod(doc, "sinal_v1    = pd.read_parquet('../../dados/sinal_v1.parquet')")

    h2(doc, "Bloco 2 — Construir Portfolio Equal-Weight")
    fala(doc, "A logica de construcao do portfolio e simples: em cada mes, identificar as top 10 acoes por sinal e dar peso igual de 10% para cada uma.")
    cod(doc, "def construir_portfolio(sinal, ret_mensais, n_top=10):")
    cod(doc, "    '''Constroi portfolio equal-weight com top N acoes por sinal'''")
    cod(doc, "    pesos = pd.DataFrame(0.0, index=sinal.index, columns=sinal.columns)")
    cod(doc, "    ")
    cod(doc, "    for data in sinal.index:")
    cod(doc, "        s = sinal.loc[data].dropna()")
    cod(doc, "        if len(s) >= n_top:")
    cod(doc, "            top = s.nlargest(n_top).index")
    cod(doc, "            pesos.loc[data, top] = 1.0 / n_top")
    cod(doc, "    ")
    cod(doc, "    # Retorno do portfolio: soma ponderada dos retornos")
    cod(doc, "    # Alinhando: pesos de t aplicados aos retornos de t+1")
    cod(doc, "    ret_portfolio = (pesos.shift(1) * ret_mensais).sum(axis=1)")
    cod(doc, "    ret_portfolio = ret_portfolio[ret_portfolio != 0]")
    cod(doc, "    ")
    cod(doc, "    return pesos, ret_portfolio")
    fala(doc, "Parem aqui — tem um detalhe critico. Usamos pesos.shift(1) na linha de calculo do retorno. Por que? Porque os pesos sao calculados no final do mes t usando o sinal formado em t. Mas o retorno que esses pesos geram e o retorno do mes t+1 — o mes seguinte ao rebalanceamento. O shift(1) garante esse alinhamento temporal correto.")
    fala(doc, "Se voce nao fizer o shift, voce esta usando os pesos do mes t para capturar o retorno do mes t, o que implicaria que voce sabe o sinal antes de tomar a decisao. E um look-ahead bias mais sutil que o do sinal em si, mas igualmente invalidante.")
    cod(doc, "pesos_v1, ret_v1 = construir_portfolio(sinal_v1, ret_mensais, n_top=10)")
