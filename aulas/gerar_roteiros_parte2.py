    fala(doc, "O Calmar Ratio e o CAGR dividido pelo valor absoluto do Max Drawdown. Ele responde: quanto de retorno anualizado eu gero por unidade de drawdown maximo? Um Calmar acima de 1 significa que o retorno anualizado e maior que o pior drawdown — isso e considerado bom.")

