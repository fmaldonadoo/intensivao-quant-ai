    tempo(doc, "0:00 – 0:35")
    div(doc)

    h2(doc, "1.1 — O Problema do Backtesting Ingênuo")
    acao(doc, "Slide 1 na tela. Tom sério. Esta aula é a mais importante do curso em termos de rigor científico.")
    fala(doc, "Pessoal, sejam bem-vindos à Aula 8 — Backtest Rigoroso e Validação Estatística.")
    fala(doc, "Nas últimas semanas vocês construíram um pipeline completo: dados, sinal, portfólio, métricas. O backtest mostrou um Sharpe de 0,8, retorno acumulado de mais de 100%. Parece ótimo.")
    fala(doc, "Mas agora vou fazer uma pergunta incômoda: o quanto desse resultado é real, e o quanto é artefato do processo de análise?")
    fala(doc, "Existe um fenômeno muito documentado em finanças quantitativas chamado overfitting — ou ajuste excessivo. Quando você testa muitas hipóteses, muitos parâmetros, muitas variações de um modelo, eventualmente você encontra algo que funcionou no passado simplesmente por acaso estatístico.")
    fala(doc, "É como jogar uma moeda 1.000 vezes e encontrar uma sequência de 10 caras seguidas. Se você olhar só essa sequência e construir uma estratégia baseada nela, vai parecer que a moeda é viesada — mas não é.")
    fala(doc, "Harvey, Liu e Zhu, em um paper de 2016, analisaram centenas de fatores publicados na literatura de finanças e concluíram que a maioria provavelmente são falsos positivos. Eles calcularam que, dado o volume de testes realizados por pesquisadores ao longo do tempo, um fator precisaria ter t-statistic acima de 3,0 — não mais 2,0 — para ser considerado estatisticamente robusto.")
    fala(doc, "Bailey e López de Prado formalizaram isso no conceito de Probability of Backtest Overfitting, ou PBO: a probabilidade de que o melhor resultado em testes de amostra seja o pior em dados fora da amostra. Eles demonstraram que, com combinatorial purged cross-validation, é possível estimar essa probabilidade diretamente.")
    fala(doc, "E Marcos López de Prado, no livro Advances in Financial Machine Learning de 2018, introduziu o Deflated Sharpe Ratio, ou DSR: uma versão do Sharpe que penaliza pela quantidade de estratégias testadas e pela não-normalidade dos retornos.")

    div(doc)
    h2(doc, "1.2 — As Três Fontes de Viés em Backtests")
    acao(doc, "Slide 2. Enumerate os três vieses, pausando em cada um.")
    fala(doc, "Existem três grandes fontes de viés que precisamos entender e controlar.")
    fala(doc, "Primeiro: Look-ahead bias, ou viés de antecipação. Ocorre quando você usa informação que não estaria disponível no momento da decisão de investimento. O exemplo mais comum: usar o retorno do mês T para construir o sinal do mês T. Na prática, você só sabe o retorno de T ao final de T — então o sinal só pode ser formado no início de T+1. No nosso código, controlamos isso com shift(1) no backtest.")
    fala(doc, "Segundo: Survivorship bias, ou viés de sobrevivência. Ocorre quando você testa sua estratégia somente em ativos que ainda existem hoje. Empresas que faliram, saíram do índice, foram incorporadas — essas não aparecem na sua amostra atual. O efeito é que você superestima o retorno histórico porque você só viu os 'sobreviventes'.")
    fala(doc, "Terceiro: Multiple testing bias, ou viés de múltiplos testes. Já falamos sobre isso. Se você testa 100 variações de um parâmetro e 5 funcionam bem, espera-se que, com 95% de confiança, 5 funcionem por acaso puro — mesmo que o verdadeiro efeito seja zero. A solução é aplicar correções estatísticas, como Bonferroni ou, mais adequado para séries financeiras, o ajuste proposto por Bailey e López de Prado.")

    div(doc)
    h2(doc, "1.3 — Walk-Forward Analysis vs In-Sample/Out-of-Sample")
    acao(doc, "Slide 3. Diagrama temporal na tela.")
    fala(doc, "A metodologia padrão em quant finance para mitigar look-ahead bias em otimização de parâmetros é o Walk-Forward Analysis.")
    fala(doc, "A ideia é simples: você divide a série temporal em janelas. Em cada janela, você tem um período de treinamento — in-sample — e um período de teste — out-of-sample. Você treina no in-sample, testa no out-of-sample, avança a janela, e repete.")
    fala(doc, "O resultado final é a concatenação dos períodos out-of-sample. Isso garante que cada predição foi feita com parâmetros calculados exclusivamente com dados disponíveis naquele momento.")
    fala(doc, "No nosso contexto, implementamos isso com a função backtest_walkforward que vocês já construíram. Ela usa 36 meses de treinamento e 6 meses de teste, avançando de 6 em 6. Os retornos líquidos finais são puramente out-of-sample.")
    fala(doc, "Uma variante mais sofisticada é o Purged Cross-Validation proposto por López de Prado: além de separar treino e teste, você remove um gap temporal entre eles — chamado 'embargo' — para eliminar qualquer vazamento de informação por sobreposição de retornos calculados em janelas adjacentes.")

    div(doc)
    h2(doc, "1.4 — Deflated Sharpe Ratio (DSR)")
    acao(doc, "Slide 4. Equação do DSR na tela.")
    fala(doc, "O Sharpe Ratio que calculamos nas aulas anteriores é o Sharpe observado: retorno médio dividido pelo desvio padrão, anualizado. É uma estatística pontual — não considera incerteza, não considera quantas estratégias testamos.")
    fala(doc, "O Deflated Sharpe Ratio corrige duas distorções: a inflação por múltiplos testes e o viés por não-normalidade.")
    fala(doc, "A fórmula envolve o Expected Maximum Sharpe Ratio, que cresce com o número de estratégias testadas. Quanto mais estratégias você testou, maior o Sharpe esperado por acaso puro — e portanto mais alto precisa ser o Sharpe observado para ser considerado genuíno.")
    fala(doc, "O DSR também corrige para skewness e kurtosis dos retornos. Retornos com cauda pesada ou assimetria negativa inflariam o Sharpe clássico — o DSR desconta isso.")
    fala(doc, "Hoje vamos implementar uma versão simplificada do DSR que captura a essência do conceito: penalização pelo número de configurações de janela testadas no walk-forward e ajuste por skewness e kurtosis.")

    div(doc)

    # =========================================================
    h1(doc, "PARTE 2 — CODIGO: BACKTEST_WALKFORWARD E VALIDACAO")
    tempo(doc, "0:35 – 1:30")
    div(doc)

    h2(doc, "2.1 — Setup Inicial")
    acao(doc, "Abrir Jupyter. Novo notebook: aula_08_backtest_rigoroso.ipynb.")
    fala(doc, "Vamos abrir o notebook da aula 8. Como sempre, começamos com os imports e carregamento de dados.")
    cod(doc, "import pandas as pd")
    cod(doc, "import numpy as np")
    cod(doc, "import matplotlib.pyplot as plt")
    cod(doc, "import matplotlib.gridspec as gridspec")
    cod(doc, "from scipy import stats")
    cod(doc, "import warnings")
    cod(doc, "warnings.filterwarnings('ignore')")
    cod(doc, "")
    cod(doc, "ret_mensais   = pd.read_parquet('../dados/retornos_mensais_limpo.parquet')")
    cod(doc, "sinal_v2      = pd.read_parquet('../dados/sinal_v2.parquet')")
    cod(doc, "pesos_v2      = pd.read_parquet('../dados/pesos_v2.parquet')")
    cod(doc, "ret_carteira  = pd.read_parquet('../dados/retorno_carteira.parquet')")
    cod(doc, "")
    cod(doc, "print(ret_mensais.shape, sinal_v2.shape)")
    fala(doc, "Carregamos os dados de todas as aulas anteriores. Agora vamos olhar o que já temos de out-of-sample no ret_carteira — este é o nosso benchmark de comparação.")

    div(doc)
    h2(doc, "2.2 — Função calcular_dsr()")
    acao(doc, "Digitar ao vivo. Explicar cada parâmetro.")
    fala(doc, "Antes de rodar o walk-forward, vamos implementar o DSR — assim já podemos aplicá-lo ao resultado.")
    cod(doc, "def calcular_dsr(retornos, n_estrategias=1):")
    cod(doc, "    '''")
    cod(doc, "    Deflated Sharpe Ratio (Bailey & Lopez de Prado, 2014).")
    cod(doc, "    Penaliza o Sharpe por multiplos testes e nao-normalidade.")
    cod(doc, "    '''")
    cod(doc, "    n  = len(retornos)")
    cod(doc, "    sr = retornos.mean() / retornos.std() * np.sqrt(12)")
    cod(doc, "    sk = retornos.skew()")
    cod(doc, "    ku = retornos.kurtosis()")
    cod(doc, "")
    cod(doc, "    # Expected max SR de n_estrategias tentativas (aprox. Extreme Value)")
    cod(doc, "    e_maxsr = np.sqrt(2) * stats.norm.ppf(1 - 1/n_estrategias) if n_estrategias > 1 else 0")
    cod(doc, "")
    cod(doc, "    # Correcao por nao-normalidade")
    cod(doc, "    sr_corr = sr * (1 - sk/6 * sr + (ku-3)/24 * sr**2)**(-0.5)")
    cod(doc, "")
    cod(doc, "    # Probabilidade de superar o expected max SR")
    cod(doc, "    dsr = stats.norm.cdf((sr_corr - e_maxsr) * np.sqrt(n - 1) /")
    cod(doc, "                         np.sqrt(1 - sr_corr * e_maxsr + sr_corr**2 * (ku-1)/4))")
    cod(doc, "    return {'sharpe_obs': sr, 'sharpe_corr': sr_corr, 'e_maxsr': e_maxsr, 'dsr': dsr}")
    fala(doc, "O retorno é um dicionário com quatro valores: o Sharpe observado, o Sharpe corrigido por não-normalidade, o Sharpe máximo esperado por acaso dado o número de estratégias testadas, e o DSR — que é uma probabilidade entre 0 e 1.")
    fala(doc, "Um DSR acima de 0,95 indica que o resultado é estatisticamente robusto mesmo após deflação. Abaixo de 0,5 levanta forte suspeita de overfitting.")

