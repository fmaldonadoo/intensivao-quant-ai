    h2(doc, "2.4 — Analise de Sensibilidade de Parametros")
    acao(doc, "Digitar. Mostrar robustez do sinal.")
    fala(doc, "Para demonstrar robustez, vamos testar o sinal com diferentes lookbacks e ver se o resultado se mantém.")
    cod(doc, "def backtest_simples(sinal, ret_mensais, n_long_pct=0.2):")
    cod(doc, "    retornos = []")
    cod(doc, "    for data in sinal.index:")
    cod(doc, "        sig = sinal.loc[data].dropna()")
    cod(doc, "        if len(sig) < 5: continue")
    cod(doc, "        ranks  = sig.rank(ascending=False)")
    cod(doc, "        n      = max(int(len(ranks) * n_long_pct), 1)")
    cod(doc, "        top    = ranks[ranks <= n].index")
    cod(doc, "        pesos  = pd.Series(1/len(top), index=top)")
    cod(doc, "        if data in ret_mensais.index:")
    cod(doc, "            ret_m = ret_mensais.loc[data, top].dropna()")
    cod(doc, "            pw    = pesos.reindex(ret_m.index).dropna()")
    cod(doc, "            pw   /= pw.sum()")
    cod(doc, "            retornos.append({'data': data, 'ret': (ret_m * pw).sum()})")
    cod(doc, "    return pd.DataFrame(retornos).set_index('data')['ret']")
    cod(doc, "")
    cod(doc, "# Testar lookbacks de 6 a 18 meses")
    cod(doc, "resultados_sensib = {}")
    cod(doc, "for lb in [6, 9, 12, 15, 18]:")
    cod(doc, "    sinal_lb = ret_mensais.shift(2).rolling(lb).sum()")
    cod(doc, "    ret_lb   = backtest_simples(sinal_lb, ret_mensais)")
    cod(doc, "    sharpe   = ret_lb.mean() / ret_lb.std() * np.sqrt(12)")
    cod(doc, "    resultados_sensib[f'Lookback {lb}m'] = round(sharpe, 2)")
    cod(doc, "")
    cod(doc, "print('Sensibilidade do Sharpe por Lookback:')")
    cod(doc, "for k, v in resultados_sensib.items():")
    cod(doc, "    print(f'  {k}: Sharpe = {v}')")
    fala(doc, "Se o Sharpe se mantiver relativamente estável entre 0,6 e 1,0 para todos os lookbacks, isso é sinal de robustez. Se só funcionar para lookback=12, levanta suspeita de overfitting.")

