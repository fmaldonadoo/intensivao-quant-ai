    cod(doc, "    '''")
    cod(doc, "    datas = sinal.index.sort_values()")
    cod(doc, "    resultados = []")
    cod(doc, "")
    cod(doc, "    inicio = janela_treino")
    cod(doc, "    while inicio + janela_teste <= len(datas):")
    cod(doc, "        treino_end = datas[inicio - 1]")
    cod(doc, "        teste_end  = datas[min(inicio + janela_teste - 1, len(datas)-1)]")
    cod(doc, "        ")
    cod(doc, "        # Periodo out-of-sample")
    cod(doc, "        datas_oos = datas[inicio:inicio + janela_teste]")
    cod(doc, "        ")
    cod(doc, "        for data in datas_oos:")
    cod(doc, "            if data in sinal.index:")
    cod(doc, "                sig = sinal.loc[data].dropna()")
    cod(doc, "                if len(sig) > 0:")
    cod(doc, "                    ranks  = sig.rank(ascending=False)")
    cod(doc, "                    n_long = max(int(len(ranks) * 0.2), 1)")
    cod(doc, "                    top    = ranks[ranks <= n_long].index")
    cod(doc, "                    pesos  = pd.Series(1/len(top), index=top)")
    cod(doc, "                    ")
    cod(doc, "                    if data in ret_mensais.index:")
    cod(doc, "                        ret_mes = ret_mensais.loc[data, pesos.index].dropna()")
    cod(doc, "                        pesos_  = pesos.reindex(ret_mes.index).dropna()")
    cod(doc, "                        pesos_ /= pesos_.sum()")
    cod(doc, "                        ret_oos = (ret_mes * pesos_).sum()")
    cod(doc, "                        resultados.append({'data': data, 'retorno': ret_oos, 'n_ativos': len(top)})")
    cod(doc, "        inicio += janela_teste")
    cod(doc, "    ")
    cod(doc, "    return pd.DataFrame(resultados).set_index('data')['retorno']")
    fala(doc, "Notem que dentro do loop só usamos dados até treino_end para qualquer decisão de parâmetros — aqui, nossos parâmetros são fixos, mas a estrutura permite variar o n_long, o lookback, qualquer hiperparâmetro. A decisão final de portfólio usa apenas o sinal daquele mês específico, sem vazamento.")

    div(doc)
    h2(doc, "2.4 — Executar Walk-Forward e Comparar")
    acao(doc, "Rodar a função. Pode demorar alguns segundos.")
    fala(doc, "Vamos rodar e comparar o resultado out-of-sample com o backtest simples.")
