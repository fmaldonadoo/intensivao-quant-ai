    cod(doc, "cum_oos = (1 + ret_oos).cumprod()")
    cod(doc, "")
    cod(doc, "ax1.plot(cum_is.index,  cum_is.values,  label='In-Sample (backtest simples)',  color='#1A6EAE', lw=2)")
    cod(doc, "ax1.plot(cum_oos.index, cum_oos.values, label='Out-of-Sample (walk-forward)', color='#F5A623', lw=2, ls='--')")
    cod(doc, "ax1.set_title('Retorno Acumulado: IS vs OOS')")
