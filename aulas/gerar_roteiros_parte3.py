    cod(doc, "print(f'Periodo OOS: {ret_oos.index.min()} a {ret_oos.index.max()}')")
    cod(doc, "print(f'Observacoes OOS: {len(ret_oos)}')")
    cod(doc, "")
    cod(doc, "# Calcular metricas do OOS")
    cod(doc, "sharpe_oos = ret_oos.mean() / ret_oos.std() * np.sqrt(12)")
    cod(doc, "cagr_oos   = (1 + ret_oos).prod() ** (12/len(ret_oos)) - 1")
    cod(doc, "")
    cod(doc, "print(f'Sharpe OOS: {sharpe_oos:.2f}')")
    cod(doc, "print(f'CAGR OOS:   {cagr_oos:.1%}')")
    fala(doc, "Espera-se que o Sharpe OOS seja um pouco menor que o in-sample — isso é normal e desejável. Significa que o modelo não está inflado. Se o OOS for igual ou maior que o in-sample, pode ser sinal de que a janela de teste é muito curta ou que você teve sorte específica.")

    div(doc)
    h2(doc, "2.5 — Aplicar o DSR")
    acao(doc, "Digitar. Mostrar como interpretar o resultado.")
    fala(doc, "Agora aplicamos o DSR. Como testamos poucas configurações — basicamente fixamos os parâmetros nas aulas anteriores —, vamos usar n_estrategias=5 como estimativa conservadora.")
    cod(doc, "dsr_result = calcular_dsr(ret_oos, n_estrategias=5)")
    cod(doc, "print('=== Deflated Sharpe Ratio ===')")
    cod(doc, "print(f'  Sharpe Observado:  {dsr_result[\"sharpe_obs\"]:.3f}')")
    cod(doc, "print(f'  Sharpe Corrigido:  {dsr_result[\"sharpe_corr\"]:.3f}')")
    cod(doc, "print(f'  E[MaxSR] esperado: {dsr_result[\"e_maxsr\"]:.3f}')")
    cod(doc, "print(f'  DSR (prob):        {dsr_result[\"dsr\"]:.3f}')")
    fala(doc, "O DSR próximo de 1 significa alta confiança de que o resultado não é artefato de overfitting. O DSR próximo de 0 seria um sinal de alerta. Para o Desafio Itaú, qualquer estratégia que vocês apresentarem deve ter DSR acima de 0,80 para ser considerada robusta.")

    div(doc)
    h2(doc, "2.6 — Visualizacao: In-Sample vs Out-of-Sample")
    acao(doc, "Gráfico comparativo. Mostrar divergência ou convergência.")
    fala(doc, "Vamos fazer um gráfico que mostra lado a lado o retorno acumulado in-sample e out-of-sample.")
