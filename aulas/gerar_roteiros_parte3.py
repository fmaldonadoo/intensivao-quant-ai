    cod(doc, "def backtest_walkforward(sinal, ret_mensais, janela_treino=36, janela_teste=6):")
    cod(doc, "    '''")
    cod(doc, "    Walk-forward analysis.")
    cod(doc, "    janela_treino: meses de treinamento para otimizacao de parametros")
